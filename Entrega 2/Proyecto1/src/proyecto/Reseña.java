package proyecto;

public class Reseña {
	
	//Atributos
	private String texto;
    private float rating;
    
    //Constructor
	public Reseña(String texto, float rating) {
		this.texto = texto;
		this.rating = rating;
	}
	
	//Get and set
	public String getTexto() {
		return texto;
	}
	public float getRating() {
		return rating;
	}
	
	//Metodos
	
}
